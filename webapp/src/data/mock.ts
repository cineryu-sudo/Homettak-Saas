export const scheduleEvents: ScheduleEvent[] = orders
  .filter((o) => o.scheduledDate && o.technicianId)
  .map((o) => ({
    id: `SCH-${o.id}`,
    orderId: o.id,
    technicianId: o.technicianId!,
    date: o.scheduledDate!,
    timeSlot: o.notes.includes("오전")
      ? "09:00-12:00"
      : o.notes.includes("오후")
        ? "14:00-17:00"
        : "10:00-13:00",
    customerName: o.customerName,
    address: o.address,
    status: o.status,
  }));
